// Concatene duas strings.

let string1 = "Olá, ";
let string2 = "mundo!";
let resultado = string1 + string2;
console.log(resultado); // Saída: Olá, mundo!



// Converta uma string para maiúsculas ou minúsculas.

let string = "Olá, mundo!";
let maiusculas = string.toUpperCase();
console.log(maiusculas); // Saída: OLÁ, MUNDO!

let string3 = "Olá, mundo!";
let minusculas = string3.toLowerCase();
console.log(minusculas); // Saída: olá, mundo!

